public  class Ingredientes {
    private String nome;
    private String quantidade;

    public Ingredientes(String nome, String quantidade) {
        this.nome = nome;
        this.quantidade = quantidade;
    }
    public String getNome() {
        return quantidade;
    }
    public String getQuantidade() {
        return quantidade;
    }

    
    
}
